import Head from 'next/head'
import {useEffect,useState} from 'react'
//import useSWR from 'swr';
//import axios from 'axios';
import { useRouter } from 'next/router'

import Toolbar from '../../global_to_local_dict_lib/toolbar'
import Autocomplete from '../../global_to_local_dict_lib/autocomplete'
import {url} from '../../global_to_local_dict_lib/rootUrl'


export default function AddWord(argument) {
  const router = useRouter()
  const [filteredSuggestions, setFilteredSuggestions] = useState([]);
  const [data, setData] = useState([])
  const [input, setInput] = useState("");
  const [local, setLocal] = useState(false);
  const [language, setLanguage] = useState("");
  const [search, setSearch] = useState('')
  const [isLoading, setLoading] = useState(false)
  const urlSearch=`${url}/language/country/?search=${search}`
  //http://localhost:8000/language/country/
  //const fetcher = (...args) => fetch(...args).then(res => res.json())
  //const { data, error } = useSWR(urlSearch, fetcher)
  
  const submitData=(e)=>{
    e.preventDefault()
    const countryUrl=data.filter(u=>u.name==input)
    const dt = {
      local,
      name:language,
      country:(countryUrl.length > 0 ? countryUrl[0].url : null),
      country_name:input
    }
    const postData =  () => {
      //const rawResponse = await 
      fetch(`${url}/language/`, {
        method: 'POST',
        headers: {
          'Accept': 'application/json',
          'Content-Type': 'application/json'
        },
        body: JSON.stringify(dt)
      })
      .then(data=>{
        if(router.query.back) router.back()
        return router.push('/languages')
      })
    }
    postData()
  }
  
  const localChange=(e)=>setLocal(e.target.checked)
  const languageChange=(e)=>setLanguage(e.target.value)

  
  useEffect(() => {
    setLoading(true)
    fetch(`${urlSearch}`)
      .then((res) => res.json())
      .then((data) => {
        setData(data.results)
        setLoading(false)
      })
  }, [input])
  
  
  
  return(
  <div className="container">
  <Head>
        <title>Create Next App</title>
        <meta charset="utf-8"/>
        <meta http-equiv="X-UA-Compatible" content="IE=edge"/>
        <meta name="viewport" content="width=device-width, initial-scale=1"/>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Toolbar/>
  <section class="h-100">
    <div class="container h-100">
      <div class="row justify-content-md-center h-100">
        <div class="card-wrapper">
          <div class="brand">
            
          </div>
          <div class="card fat">
            <div class="card-body container img-thumbnail">
              <h3 class="card-title">Add Language</h3>
              <form onSubmit={submitData} method="POST" class="my-login-validation" novalidate="">
                
                <div class="form-group">
                  <label for="name">Language Name</label>
                  <input value={language} onChange={languageChange} id="name" type="text" class="form-control" name="name" required autofocus/>
                  <div class="invalid-feedback">
                    What's language name?
                  </div>
                </div>
                <div class="form-group">
                  <label for="country">Country</label>
                  <Autocomplete setInput={setInput} input={input} suggestions={data.map(l=>l.name)}/>
                  <div class="invalid-feedback">
                    Country the language belongs
                  </div>
                </div>
                <div class="form-group">
                  <div class="custom-checkbox custom-control">
                    <label for="local" class="custom-control-label">local language</label>
                    <input value={local} onChange={localChange} type="checkbox" name="local" id="local" class="custom-control-input" required=""/>
                    <div class="invalid-feedback">
                      Check if the language is local
                    </div>
                  </div>
                </div>
                <div class="form-group m-0">
                  <button type="submit" class="btn btn-primary btn-block">
                    ADD LANGUAGE
                  </button>
                </div>
                
              </form>
            </div>
          </div>
          
        </div>
      </div>
    </div>
  </section>
  </div>
    )
}